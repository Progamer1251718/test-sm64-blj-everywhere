There are some videos on YT that do have this information but yes you'd expect there to be more info readily available. For Lakitu skip there is a video on YT: <!-- m --><a class="postlink" href="https://www.youtube.com/watch?v=K0aY_HY48zQ" target="_blank">https://www.youtube.com/watch?v=K0aY_HY48zQ</a><!-- m --> ..In regards to bomb clip, you want to be able to throw the bomb so to get backwards momentum to clip through, so Mario facing perpendicular to the cage, essentially. For grabbing the bomb, when you throw it, the more time you take to re-grab, the bigger the bomb will be due to it being in explosion animation. Big bomb is preferred so waiting longer to grab is better, but is close to exploding so timing practice is necessary.<br /><br />People normally watch videos of strats with controller inputs then spend time practicing and learning through trial and error and then finally understanding the trick. Don't just watch top players or their videos, I recommend to watch other new players also and see what strats they use, otherwise you're gonna be trying strats that you can't do and make it harder for yourself. If you do have any more questions regarding why or what, come into the practice room some time, people will answer them there.<p>Statistics: Posted by <a href="http://marioruns.com/forum/memberlist.php?mode=viewprofile&amp;u=125">GothicLogic</a> — 12 Aug 2014, 20:32</p><hr />
